package Camera;

public class location {
    private double x;
    private double y;

    public location(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public location() {

    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
